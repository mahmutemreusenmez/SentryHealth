  let current = 'tr';

  function getCurrentLang() {
    return current;
  }

  function setLanguage(lang) {
    if (!translations[lang]) {
      lang = 'tr';
    }
    current = lang;
    localStorage.setItem('sentry-lang', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    if (document.body) {
      document.body.classList.toggle('rtl', lang === 'ar');
    }
    document.title = t('app.title');
    applyTranslations();
  }

  function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const text = t(el.dataset.i18n);
      if (text !== el.dataset.i18n) {
        el.textContent = text;
      }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-title]').forEach((el) => {
      el.title = t(el.dataset.i18nTitle);
    });
    document.querySelectorAll('[data-i18n-value]').forEach((el) => {
      el.value = t(el.dataset.i18nValue);
    });
    document.querySelectorAll('[data-i18n-html]').forEach((el) => {
      el.innerHTML = t(el.dataset.i18nHtml);
    });
  }

  function t(key, vars = {}) {
    if (typeof key !== 'string') return '';
    const parts = key.split('.');
    let value = parts.reduce((obj, part) => (obj && obj[part] !== undefined ? obj[part] : undefined), translations[current]);
    if (typeof value !== 'string') {
      value = parts.reduce((obj, part) => (obj && obj[part] !== undefined ? obj[part] : undefined), translations['tr']);
    }
    if (typeof value !== 'string') {
      return key;
    }
    return value.replace(/\{(\w+)\}/g, (_, name) => (vars[name] !== undefined ? vars[name] : `{${name}}`));
  }

